    private void b_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_agregarActionPerformed
        
        
        itemcombmet = comb_rep_ent.getSelectedItem().toString();
        itemcombcal = comb_rep_cal.getSelectedItem().toString();
        
        if(tx_rep_n.getText().isEmpty() || tx_rep_ap.getText().isEmpty() || tx_rep_am.getText().isEmpty() || 
           tx_rep_cor.getText().isEmpty() || tx_rep_tel.getText().isEmpty() || tx_rep_u.getText().isEmpty() ||
           tx_rep_contra.getText().isEmpty() || itemcombmet.equals("Seleccionar") || 
           itemcombcal.equals("Seleccionar")){
           JOptionPane.showMessageDialog(null, "Deben estar llenos todos los campos" );
        }else{
           // Cliente cnt =  new Cliente();
           String met, cal;
       
            if(itemcombmet.equals("A Pie")){
                met = "A Pie";
            }else if(itemcombmet.equals("Automovil")){
                met = "Automovil";
            }else if(itemcombmet.equals("Bicicleta")){
                met = "Bicicleta";
            }else{
                met = "Motocicleta";
            }
            
            if(itemcombcal.equals("1 Estrella")){
                cal = "1 Estrella";
            }else if(itemcombcal.equals("2 Estrellas")){
                cal = "2 Estrellas";
            }else if(itemcombcal.equals("3 Estrellas")){
                cal = "3 Estrellas";
            }else if(itemcombcal.equals("4 Estrellas")){
                cal = "4 Estrellas";
            }else{
                cal = "5 Estrellas";
            }
            rep.setRep_nom(tx_rep_n.getText());
            rep.setRep_ap(tx_rep_ap.getText());
            rep.setRep_am(tx_rep_am.getText());
            rep.setRep_correo(tx_rep_cor.getText());
            rep.setRep_tel(Long.parseLong(tx_rep_tel.getText()));
            rep.setRep_usu(tx_rep_u.getText());
            rep.setRep_contra(tx_rep_contra.getText());
            rep.setRep_met_ent(met);
            rep.setRep_cal(cal);
            String mensaje = rBO.agregarRep(rep);
            limpiar();
            mostrarRepartidoresT();
        }
    }//GEN-LAST:event_b_agregarActionPerformed

